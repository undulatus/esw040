package com.pointwest.workforce.planner.domain;

public class WorkbookException {

	public WorkbookException() {
		super();
	}

	private String errorMessage;
	private String rowNumber;
	private String columnHeader;
	private String missingRequiredColumnHeader;

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}

	public String getRowNumber() {
		return rowNumber;
	}

	public void setRowNumber(String rowNumber) {
		this.rowNumber = rowNumber;
	}

	public String getColumnHeader() {
		return columnHeader;
	}

	public void setColumnHeader(String columnHeader) {
		this.columnHeader = columnHeader;
	}

	public String getMissingRequiredColumnHeader() {
		return missingRequiredColumnHeader;
	}

	public void setMissingRequiredColumnHeader(String missingRequiredColumnHeader) {
		this.missingRequiredColumnHeader = missingRequiredColumnHeader;
	}

}
